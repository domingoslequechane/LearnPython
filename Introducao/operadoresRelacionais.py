'''Operadores relacionais podem responder apenas dois valoresa (True ou False)'''
'''Operadores:
    > -> maior
    < -> menor
    == -> igual
    != -> diferente
    >= -> maior ou igual
    <= -> menor ou igual
    
    NOTA: = é um operadores de atribuição
'''

operador = 2 > 3
print(operador)

operador = 2 < 3
print(operador)

operador = 2 == 3
print(operador)

operador = 2 != 3
print(operador)

operador = 2 >= 3
print(operador)

operador = 2 <= 3
print(operador)

operador = 2 <= 2
print(operador)